using kyc360_assignment_rahul_m.Controllers;
using kyc360_assignment_rahul_m.Repositories;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

//dependency injection (if IEntityRepository instance is requested, an instance of MockEntityRepository will be assigned (it is created once throughout the lifecycle))
builder.Services.AddSingleton<IEntityRepository, MockEntityRepository>();


var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
